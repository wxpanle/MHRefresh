+ (CGFloat)getStringHeightWithString:(NSString *)string width:(CGFloat)width font:(UIFont *)font {
    return [self getSizeWithString:string font:font appointSize:CGSizeMake(width, CGFLOAT_MAX)].height;
}

+ (CGSize)getSizeWithString:(NSString *)string font:(UIFont *)font appointSize:(CGSize)appointSize {
    if (nil == font) {
        font = [UIFont systemFontOfSize:[UIFont systemFontSize]];
    }
    
    __autoreleasing NSString *tempString = [string copy];
    
    if (nil == tempString || !tempString.length) {
        tempString = @"找不见字符串";
    }
    
    NSStringDrawingOptions options =  NSStringDrawingUsesLineFragmentOrigin | NSStringDrawingUsesFontLeading;
    CGRect frame = [tempString boundingRectWithSize:appointSize options:options attributes:@{NSFontAttributeName : font} context:nil];
    
    CGSize resultSize = frame.size;
    NSInteger number = (NSInteger)(frame.size.height + 1.5);
    resultSize.height = (CGFloat)number;
    return resultSize;
}

- (CGFloat)getStringHeightWithWidth:(CGFloat)width font:(UIFont *)font {
    return [[self class] getStringHeightWithString:self width:width font:font];
}

- (CGSize)getSizefont:(UIFont *)font appointSize:(CGSize)appointSize {
    return [[self class] getSizeWithString:self font:font appointSize:appointSize];
}

- (CGFloat)heightWithFont:(UIFont *)font constrainedToWidth:(CGFloat)width {
    
    UIFont *textFont = font != nil ? font : [UIFont systemFontOfSize:[UIFont systemFontSize]];
    
    CGSize textSize;
    
#if __IPHONE_OS_VERSION_MIN_REQUIRED < 70000
    if ([self respondsToSelector:@selector(boundingRectWithSize:options:attributes:context:)]) {
        NSMutableParagraphStyle *paragraph = [[NSMutableParagraphStyle alloc] init];
        paragraph.lineBreakMode = NSLineBreakByWordWrapping;
        NSDictionary *attributes = @{NSFontAttributeName: textFont,
                                     NSParagraphStyleAttributeName: paragraph};
        textSize = [self boundingRectWithSize:CGSizeMake(width, CGFLOAT_MAX)
                                      options:(NSStringDrawingUsesLineFragmentOrigin |
                                               NSStringDrawingTruncatesLastVisibleLine)
                                   attributes:attributes
                                      context:nil].size;
    } else {
        textSize = [self sizeWithFont:textFont
                    constrainedToSize:CGSizeMake(width, CGFLOAT_MAX)
                        lineBreakMode:NSLineBreakByWordWrapping];
    }
#else
    NSMutableParagraphStyle *paragraph = [[NSMutableParagraphStyle alloc] init];
    paragraph.lineBreakMode = NSLineBreakByWordWrapping;
    NSDictionary *attributes = @{NSFontAttributeName: textFont,
                                 NSParagraphStyleAttributeName: paragraph};
    textSize = [self boundingRectWithSize:CGSizeMake(width, CGFLOAT_MAX)
                                  options:(NSStringDrawingUsesLineFragmentOrigin |
                                           NSStringDrawingTruncatesLastVisibleLine)
                               attributes:attributes
                                  context:nil].size;
#endif
    
    return ceil(textSize.height);
}

